namespace Proxylity.RaptorQ;

/// <summary>
/// RFC 6330 static tables and derived parameters for one source block.
/// Covers §5.3 (parameter derivation), §5.3.5 (Rand/Deg/Tuple generators),
/// §5.5 (PRNG tables V0-V3), and §5.6 (Table 2).
/// </summary>
public readonly record struct RaptorQParameters(
    int K_prime,   // systematic symbol count (≥ K, from Table 2)
    int J,         // systematic index (Table 2)
    int S,         // LDPC symbols
    int H,         // HDPC symbols
    int W,         // LT symbols
    int L,         // L = K'+S+H  (total intermediate symbols)
    int P,         // P = L-W  (PI symbols)
    int P1,        // smallest prime ≥ P
    int U,         // U = P-H
    int B          // B = W-S  (systematic part of LT)
)
{
    // -----------------------------------------------------------------------
    // RFC §5.3  –  derive parameters from source symbol count K (1 ≤ K ≤ 56403)
    // -----------------------------------------------------------------------
    public static RaptorQParameters Compute(int K)
    {
        // Binary-search Table 2 for smallest K' ≥ K
        var kp = Table2_Kprime;
        int lo = 0, hi = kp.Length - 1;
        while (lo < hi)
        {
            int mid = (lo + hi) >> 1;
            if (kp[mid] < K) lo = mid + 1; else hi = mid;
        }

        int K_prime = kp[lo];
        int J   = Table2_J[lo];
        int S   = Table2_S[lo];
        int H   = Table2_H[lo];
        int W   = Table2_W[lo];
        int L   = K_prime + S + H;
        int P   = L - W;
        int P1  = Table2_P1[lo];
        int U   = P - H;
        int B   = W - S;
        return new(K_prime, J, S, H, W, L, P, P1, U, B);
    }

    // -----------------------------------------------------------------------
    // RFC §5.3.5.1  –  Rand[y, i, m]
    //   Returns a pseudo-random integer in [0, m) based on V0..V3 tables.
    // -----------------------------------------------------------------------
    public static uint Rand(uint y, int i, uint m)
    {
        var v0 = VTable(0); var v1 = VTable(1);
        var v2 = VTable(2); var v3 = VTable(3);
        uint x0 = (y + (uint)i) % 256;
        uint x1 = ((y >> 8) + (uint)i) % 256;
        uint x2 = ((y >> 16) + (uint)i) % 256;
        uint x3 = ((y >> 24) + (uint)i) % 256;
        uint result = v0[(int)x0] ^ v1[(int)x1] ^ v2[(int)x2] ^ v3[(int)x3];
        return result % m;
    }

    // -----------------------------------------------------------------------
    // RFC §5.3.5.2  –  Deg[v]  (degree distribution, Table 1)
    // -----------------------------------------------------------------------
    public static int Deg(uint v)
    {
        // Table 1: cumulative f values for degrees 1..40.
        // f[d] is the upper bound; degree is the first d with v < f[d].
        // Indices map to degree values: 0→1, 1→2, 2→3, 3→4, 4→10, 5→11, 6→40.
        const uint f0 = 10241;
        const uint f1 = 491582;
        const uint f2 = 712794;
        const uint f3 = 831695;
        const uint f4 = 948446;
        const uint f5 = 1032189;
        // v in [0, 2^20) per RFC; map from range [0, 2^20)
        if (v < f0)  return 1;
        if (v < f1)  return 2;
        if (v < f2)  return 3;
        if (v < f3)  return 4;
        if (v < f4)  return 10;
        if (v < f5)  return 11;
        return 40;
    }

    // -----------------------------------------------------------------------
    // RFC §5.3.5.4  –  Tuple[K', ISI]
    //   Returns (d, a, b, d1, a1, b1) for encoding symbol with ISI = X.
    // -----------------------------------------------------------------------
    public static (int d, int a, int b, int d1, int a1, int b1)
        Tuple(int K_prime, int isi, in RaptorQParameters p)
    {
        uint A = (uint)(53591 + LookupJ(K_prime) * 997);
        if (A % 2 == 0) A++;
        uint B2 = 10267 * ((uint)LookupJ(K_prime) + 1);
        uint y = (uint)((B2 + (ulong)isi * A) & 0xFFFFFFFF);
        uint v = Rand(y, 0, 1048576u);          // 2^20
        int d  = Deg(v);
        int a  = (int)(1 + Rand(y, 1, (uint)(p.W - 1)));
        int b  = (int)Rand(y, 2, (uint)p.W);
        int d1 = (d < 4) ? (2 + (int)Rand((uint)isi, 3, 2u)) : 2;
        int a1 = (int)(1 + Rand((uint)isi, 4, (uint)(p.P1 - 1)));
        int b1 = (int)Rand((uint)isi, 5, (uint)p.P1);
        return (d, a, b, d1, a1, b1);
    }

    // Convenience overload – looks up J from Table 2 via K_prime index.
    private static int LookupJ(int K_prime)
    {
        var kp = Table2_Kprime;
        int lo = 0, hi = kp.Length - 1;
        while (lo < hi)
        {
            int mid = (lo + hi) >> 1;
            if (kp[mid] < K_prime) lo = mid + 1; else hi = mid;
        }
        return Table2_J[lo];
    }

    // -----------------------------------------------------------------------
    // RFC §5.4.4  –  ENC[] walk used by encoder/decoder to build A matrix rows
    //   Returns the set of intermediate symbol indices XOR-ed to produce ESI.
    // -----------------------------------------------------------------------
    public static void EncSymbolIndices(int K_prime, int isi, in RaptorQParameters p,
                                        Span<int> indices, out int count)
    {
        var (d, a, b, d1, a1, b1) = Tuple(K_prime, isi, p);
        int n = 0;

        // RFC §5.4.2.1: LT walk visits all W positions (0..W-1) without skipping.
        // No guard on b — LDPC columns (B..W-1) are valid LT targets.
        indices[n++] = b;
        for (int j = 1; j < d; j++)
        {
            b = (b + a) % p.W;
            indices[n++] = b;
        }

        while (b1 >= p.P) b1 = (b1 + a1) % p.P1;
        indices[n++] = p.W + b1;
        for (int j = 1; j < d1; j++)
        {
            b1 = (b1 + a1) % p.P1;
            while (b1 >= p.P) b1 = (b1 + a1) % p.P1;
            indices[n++] = p.W + b1;
        }

        count = n;
    }

